package com.example.cs_office.Config;

import com.example.cs_office.Model.Branch;
import com.example.cs_office.Repository.BranchRepotitory;
import org.springframework.boot.CommandLineRunner;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.Date;
import java.util.List;

@Configuration
public class BranchConfig {
    @Bean
    CommandLineRunner commandLineRunner(BranchRepotitory branchRepotitory){
        return args -> {
            Branch branch = new Branch(1,"Thanh Xuan","98 Thanh Xuan",new Date(), true);
            branchRepotitory.saveAll(List.of(branch));
        };
    }
}
