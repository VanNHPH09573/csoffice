package com.example.cs_office.Config;

import com.example.cs_office.Model.Customer;
import com.example.cs_office.Model.Role;
import com.example.cs_office.Repository.CustomerRepository;
import com.example.cs_office.Repository.RoleRepository;
import org.springframework.boot.CommandLineRunner;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.Date;
import java.util.List;

@Configuration
public class RoleConfig {
    @Bean
    CommandLineRunner commandLineRunner(RoleRepository repository){
        return args -> {
            Role role = new Role(
                    4, "Bao", new Date(), true
            );

            repository.findRoleById(4);
            repository.saveAll(List.of(role));
//            repository.saveAndFlush(customer2);
//            repository.deleteById(2);
        };
    }
}
